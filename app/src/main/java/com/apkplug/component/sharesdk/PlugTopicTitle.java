package com.apkplug.component.sharesdk;

public class PlugTopicTitle {
	private String topicId;
	private String topicTitle;
	private String topicPublishTime;
	private String topicAuthor;
	/**
	 * @return topicId
	 */
	public String getTopicId() {
		return topicId;
	}
	/**
	 * @param topicId 要设置的 topicId
	 */
	public void setTopicId(String topicId) {
		this.topicId = topicId;
	}
	/**
	 * @return topicTitle
	 */
	public String getTopicTitle() {
		return topicTitle;
	}
	/**
	 * @param topicTitle 要设置的 topicTitle
	 */
	public void setTopicTitle(String topicTitle) {
		this.topicTitle = topicTitle;
	}
	/**
	 * @return topicPublishTime
	 */
	public String getTopicPublishTime() {
		return topicPublishTime;
	}
	/**
	 * @param topicPublishTime 要设置的 topicPublishTime
	 */
	public void setTopicPublishTime(String topicPublishTime) {
		this.topicPublishTime = topicPublishTime;
	}
	/**
	 * @return topicAuthor
	 */
	public String getTopicAuthor() {
		return topicAuthor;
	}
	/**
	 * @param topicAuthor 要设置的 topicAuthor
	 */
	public void setTopicAuthor(String topicAuthor) {
		this.topicAuthor = topicAuthor;
	}
}
